public class AssignOperTest {
    public static void main(String[] args) {
        // 1씩 증가하는 수식
        int a=1;
        a++;
        ++a;
        a=a+1;
        a+=1;
        System.out.println("a = " + a);

        int b=7;
        b--;
        --b;
        b=b-1;
        b-=1;
        System.out.println("b = " + b);


    }
}
